CREATE TABLE UNIDADE_CONSERVACAO(
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY,
    NOME VARCHAR2(255) NOT NULL,
    ID_BIOMA_IBGE NUMBER NOT NULL,
    ID_TIPO_UNIDADE_CONSERVACAO NUMBER NOT NULL,
    ID_ESFERA NUMBER NOT NULL,
    FOREIGN KEY (ID_ESFERA) REFERENCES ESFERA(ID),
    FOREIGN KEY (ID_BIOMA_IBGE) REFERENCES BIOMA_IBGE(ID),
    FOREIGN KEY (ID_TIPO_UNIDADE_CONSERVACAO) REFERENCES TIPO_UNIDADE_CONSERVACAO(ID),
    PRIMARY KEY(ID)
);